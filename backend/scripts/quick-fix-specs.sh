#!/bin/bash
# Быстрое исправление специализаций - устанавливает все специализации всем мастерам

cd "$(dirname "$0")/.."

echo "🔧 Быстрое исправление специализаций"
echo "===================================="
echo ""

# Все доступные специализации
ALL_SPECS='["Холодильник","Стиральная машина","Посудомоечная машина","Духовой шкаф","Варочная панель","Микроволновая печь","Кондиционер","Кофемашина","Ноутбук","Десктоп","Морозильный ларь","Водонагреватель"]'

echo "📋 Устанавливаем все специализации для всех мастеров..."
echo ""

sqlite3 database/database.sqlite <<EOF
UPDATE masters SET specialization = '$ALL_SPECS';
SELECT 'Обновлено мастеров: ' || changes();
EOF

echo ""
echo "✅ Готово!"
echo ""
echo "Проверка:"
node scripts/diagnose-assignments.js | grep -A 2 "Специализации"
